let val;
val = Math.PI; 
console.log(val);    // 3.141592653589793
val = Math.ceil(2.4);
console.log(val);    // 3
val = Math.floor(2.8);
console.log(val);    // 2
val = Math.round(2.3);   
console.log(val);    // 2
val = Math.sqrt(64);
console.log(val);    // 8
val = Math.abs(-4);
console.log(val);    // 4
val = Math.pow(8, 2);
console.log(val);    // 64
val = Math.min(2, 33, 44, 55, 66, -4);
console.log(val);    // -4
val = Math.max(2, 33, 44, 55, 66, -4);
console.log(val);    // 66
