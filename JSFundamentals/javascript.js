const num1 = 100;
const num2 = 50;
let val;
// addition
val = num1 + num2;    
console.log(val);    // 150
// subtraction
val = num1 - num2; 
console.log(val);    // 50
// multiply
val = num1 * num2; 
console.log(val);    // 5000
// divide
val = num1 / num2; 
console.log(val);    // 2
// modules
val = num1 % num2; 
console.log(val);    // 0
// increment - decrement
val = 1;
val++;
console.log(val);    // 2
++val;
console.log(val);    // 3
val--;
console.log(val);    // 2

