let val = 15;
console.log(val);    // 15

// += add and assign operator
val += 5;
console.log(val);    // 20

// -= sibtract and assign operator
val -= 5;
console.log(val);    // 15

// *= multiply and assign operator
val *= 5;
console.log(val);    // 75

// /= divide and assign operator
val /= 5;
console.log(val);    // 15

// %= modulus and assign operator
val %= 5;
console.log(val);    // 0


