// switch - specify many alternatives
let x = 5;
switch (x) {
    case 1:
        console.log("x is equal to 1");   
        break;
    case 2:
        console.log("x is equal to 2");   
        break;
    case 3:
        console.log("x is equal to 3");   
        break;
    case 4:
        console.log("x is equal to 4");   
        break;
    case 5:
        console.log("x is equal to 5");   
        break;
    default:
        console.log("No match found."); 
}    // x is equal to 5